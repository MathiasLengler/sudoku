use anyhow::{Context, ensure};
use itertools::Itertools;
use std::{fs::read_dir, path::Path, process::Command};
use sudoku::error::{Error, Result};

fn main() -> Result<()> {
    let binding_path = Path::new(env!("CARGO_MANIFEST_DIR")).join("bindings");

    println!("deleting bindings dir");
    if let Err(err) = std::fs::remove_dir_all(&binding_path) {
        if err.kind() != std::io::ErrorKind::NotFound {
            return Err(Error::from(err).context("unexpected error while deleting bindings dir"));
        } else {
            println!("bindings already deleted/missing");
        }
    };

    println!("running cargo test");
    let cargo_test_status = Command::new("cargo")
        .args(["test", "--features", "wasm"])
        .current_dir(env!("CARGO_MANIFEST_DIR"))
        .status()
        .context("cargo test failed")?;

    ensure!(cargo_test_status.success());

    let index_header = r#"// This file was generated by `sudoku-rs\src\bin\generate_bindings_index.rs`. Do not edit this file manually.
// Re-exports all binding types in this folder.

"#;

    let entries = read_dir(&binding_path)
        .context("expected cargo test to recrate bindings dir")?
        .map(|res| res.map_err(Error::from))
        .collect::<Result<Vec<_>>>()?;

    let mut index_content = index_header.to_owned();
    index_content.extend(
        entries
            .into_iter()
            .map(|entry| {
                entry
                    .path()
                    .file_stem()
                    .unwrap()
                    .to_os_string()
                    .into_string()
                    .unwrap()
            })
            .map(|type_name| format!("export type * from \"./{type_name}\";\n"))
            .sorted(),
    );

    println!("{}", index_content);

    std::fs::write(binding_path.join("index.ts"), index_content)?;

    Ok(())
}
