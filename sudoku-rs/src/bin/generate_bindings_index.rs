use anyhow::Context;
use itertools::Itertools;
use std::{fs::read_dir, path::Path};
use sudoku::error::{Error, Result};

fn main() -> Result<()> {
    let index_header = r#"// This file was generated by `sudoku-rs\src\bin\generate_bindings_index.rs`. Do not edit this file manually.
// Re-exports all binding types in this folder.

"#;

    let binding_path = Path::new(env!("CARGO_MANIFEST_DIR")).join("bindings");

    let entries = read_dir(&binding_path)
        .context("bindings missing, try running 'cargo test'")?
        .map(|res| res.map_err(Error::from))
        .collect::<Result<Vec<_>>>()?;

    let mut index_content = index_header.to_owned();
    index_content.extend(
        entries
            .into_iter()
            .map(|entry| {
                entry
                    .path()
                    .file_stem()
                    .unwrap()
                    .to_os_string()
                    .into_string()
                    .unwrap()
            })
            .filter(|file_stem| file_stem != "index")
            .map(|type_name| format!("export type * from \"./{type_name}\";\n"))
            .sorted(),
    );

    println!("{}", index_content);

    std::fs::write(binding_path.join("index.ts"), index_content)?;

    Ok(())
}
